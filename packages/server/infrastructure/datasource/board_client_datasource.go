package datasource

import (
	"context"
	"server/domain/model"
	"server/infrastructure/ent"
	"server/infrastructure/ent/board"

	"github.com/mitchellh/mapstructure"
)

type BoardClientDatasource struct {
	client *ent.Client
}

func NewBoardClientDatasource(client *ent.Client) *BoardClientDatasource {
	return &BoardClientDatasource{client: client}
}

func (ds *BoardClientDatasource) Create(ctx context.Context, b *model.Board) (*model.Board, error) {
	boardBuilder := ds.client.Board.Create().
		SetTitle(b.Title).
		SetUserId(b.UserId).
		SetStatus(int(b.Status)).
		AddThreads()
	if b.Description != nil {
		boardBuilder.SetDescription(*b.Description)
	}
	if b.ThumbnailUrl != nil {
		boardBuilder.SetThumbnailUrl(*b.ThumbnailUrl)
	}

	savedBoard, err := boardBuilder.Save(ctx)
	if err != nil {
		return nil, err
	}

	threadBuilder := ds.client.Thread.Create().
		SetBoard(savedBoard).
		SetTitle(b.Threads[0].Title).
		SetUserId(b.Threads[0].UserId).
		SetIsAutoGenerated(b.Threads[0].IsAutoGenerated).
		SetIsNotifyOnComment(b.Threads[0].IsNotifyOnComment).
		SetIPAddress(b.Threads[0].IpAddress).
		SetStatus(int(b.Threads[0].Status))
	if b.Threads[0].Description != nil {
		threadBuilder.SetDescription(*b.Threads[0].Description)
	}

	_, err = threadBuilder.Save(ctx)
	if err != nil {
		return nil, err
	}

	modelBoard, err := entBoardToModelBoard(savedBoard)
	if err != nil {
		return nil, err
	}

	return modelBoard, nil
}

func (ds *BoardClientDatasource) GetByTitle(ctx context.Context, title string) ([]*model.Board, error) {
	boards, err := ds.client.Board.Query().Where(board.TitleEQ(title)).All(ctx)
	if err != nil {
		return nil, err
	}

	var modelBoards []*model.Board
	for _, entBoard := range boards {
		modelBoard, err := entBoardToModelBoard(entBoard)
		if err != nil {
			return nil, err
		}
		modelBoards = append(modelBoards, modelBoard)
	}

	return modelBoards, nil
}

func entBoardToModelBoard(entBoard *ent.Board) (*model.Board, error) {
	var modelBoard model.Board
	err := mapstructure.Decode(entBoard, &modelBoard)
	if err != nil {
		return nil, err
	}
	return &modelBoard, nil
}
